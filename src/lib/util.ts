
export namespace platform {
  export const isMobile = navigator.maxTouchPoints > 0;

  export function from<T>(name: string, map: Record<string, T>): T {
    const env = map[window.APP]
    if (!env) {
      throw new Error(`"${window.APP}" not implemented for "${name}"`)
    }
    return env;
  }

}

export namespace logger {
  const LOG = Symbol("log:log");
  const ERROR = Symbol("log:error");

  export const logs: [Symbol,string][] = [];

  export function isError(symbol: Symbol) {
    return symbol == ERROR
  }

  export function setup(capture?: boolean) {
    const isCustomLog = capture ?? platform.isMobile;
    const isViteHmr = Boolean(import.meta.hot);
    if (isCustomLog && isViteHmr) {
      console.log = function(...args) {
        import.meta.hot!.send('app:log', args.map(e => JSON.stringify(e)))
      }
      console.error = function(...args) {
        import.meta.hot!.send('app:err', args.map(e => JSON.stringify(e)))
      }
    }

    console.log("logger setup; isCustomLog:",isCustomLog,"isViteHmr:",isViteHmr)
  }
}

